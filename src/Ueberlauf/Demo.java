package Ueberlauf;

public class Demo {

	public static void main(String[] args) {
		
		System.out.println("________________INT_____________________");
		System.out.println(Integer.MAX_VALUE);
		System.out.println(Integer.MIN_VALUE);

		System.out.println("___________________________________________");
		System.out.println("2147483647 + 0 =  ");
		
		int a = 2147483647;						//	MAX VALUE INT
		int b = 0;								
		
		System.out.println(a + b);
		
		System.out.println("___________________________________________");
		System.out.println("2147483647 + 1 =  ");
		
		int c = 2147483647;						//	MAX VALUE INT
		int d = 1;								
		
		System.out.println(c + d);
		
		
		System.out.println("___________________________________________");
		System.out.println("2147483647 + 2 =  ");
		
		int e = 2147483647;						//	MAX VALUE INT
		int f = 2;								
		
		System.out.println(e + f);
		
		System.out.println("_________________SHORT____________________");
		System.out.println(Short.MAX_VALUE);
		System.out.println(Short.MIN_VALUE);
		
		System.out.println("_________________BYTE____________________");
		System.out.println(Byte.MAX_VALUE);
		System.out.println(Byte.MIN_VALUE);
		
	}

}
